package BWD;

public class studentpro {
	
	int id;
	String name;
	String branch;
	char gender;
	int age;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public char getGender() {
		return gender;
	}
	public void setGender(char gender) {
		this.gender = gender;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	@Override
	public String toString() {
		return "studentpro [id=" + id + ", name=" + name + ", branch=" + branch + ", gender=" + gender + ", age=" + age
				+ "]";
	}
	
	

}
